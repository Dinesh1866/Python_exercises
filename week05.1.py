# Strings

'''slicing strings
slicing is a way to get a part of a string
slicing is done by using the index of the string
slicing is done by using the colon operator
slicing is done by using the start index and the end index
''' 
s="python"
s[0:3] #gives "pyt"

s="python"
s[0:6] #gives "python"

s="python"
s[0:7] #gives "python"
#slice operator is not giving an error if the index is out of range

s="python"
s[-1:-4] #gives ""

s="python"
s[-1:-4:-1] #gives "noh"


